    public void preencheCombo(){
        try{      
           cbClientes.removeAllItems();
           cbClientes.addItem("SELECIONE O CLIENTE");
           lista = ClientesDAO.pesquisar();
           for(Clientes c: lista){
               cbClientes.addItem(c.getNome());
           }           
        }catch(SQLException e){
            System.out.println("Erro ao inserir dados na ComboBox");
        }
    }
    
    public Venda retornaObjeto(){
        Venda v= new Venda();
        
        
        Clientes cliSelec= lista.get(cbClientes.getSelectedIndex() -1);
        v.setCliente(cliSelec);
        if(Double.parseDouble(txValor.getText()) == 0.0 || txValor.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Valor n√£o pode ser nulo");
        }else{
            v.setValorTotal(Double.parseDouble(txValor.getText()));
        }
        
        Calendar c= Calendar.getInstance();
        v.setData(c);
        
        return v;
    }

