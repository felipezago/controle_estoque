    private void btnFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarActionPerformed
        try {
            VendaDAO.inserir(retornaObjeto());
            //  venda= VendaDAO.buscaId();
            
            int a = VendaDAO.buscaId();
            
            
            System.out.println(a);
            
            ItensVendaDAO.salvaVenda(a);
            ItensVendaDAO.alteraStatus(a);
            

            int opc = JOptionPane.showConfirmDialog(null, "Deseja imprimir o pedido?");

            if(opc == JOptionPane.YES_OPTION){
                Relatorio.gerarRelatorio("rel\\V.jasper", VendaDAO.retornaRS01(a));
            }else{
                JOptionPane.showMessageDialog(null, "Venda finalizada com sucesso!");
            }
            

            this.dispose();
        } catch (SQLException ex) {
            try {
                VendaDAO.excluir(retornaObjeto());
                ex.printStackTrace();
            } catch (SQLException ex1) {
                Logger.getLogger(FinalizaVenda.class.getName()).log(Level.SEVERE, null, ex1);
            }
            Logger.getLogger(FinalizaVenda.class.getName()).log(Level.SEVERE, null, ex);
        }catch(ArrayIndexOutOfBoundsException e){
            JOptionPane.showMessageDialog(null, "Por favor selecione um cliente");
        }
        
        
        
    }//GEN-LAST:event_btnFinalizarActionPerformed

