    public void preencheCampos(Veiculo veiculo){
        txNome.setText(veiculo.getNome());
        txtPlaca.setText(veiculo.getPlaca());
        
    }
    
    public Veiculo retornaObjeto(){
        Veiculo v= new Veiculo();
        v.setNome(txNome.getText());
        v.setPlaca(txtPlaca.getText());
        v.setId(id);
        return v;
    }
    
    public boolean validaCampos(){
        boolean validador = true;

        txNome.setText(txNome.getText().trim());
        
        if(txNome.getText().equals("")){
            
            //ele ira validar se o tx esta preenchido
            validador = false;
            
            //deixa a borda vermelha se nao preenchido nada
            txNome.setBorder(BorderFactory.createLineBorder(Color.red));
            JOptionPane.showMessageDialog(null,"Preencher Campos Obrigatorios (Nome)");
        }else {
            
             txNome.setBorder(MetalBorders.getTextFieldBorder());
        }
        
        if(txtPlaca.getText().equals("    -    ")){

            validador = false;
            
            txtPlaca.setBorder(BorderFactory.createLineBorder(Color.red));
            JOptionPane.showMessageDialog(null,"Preencher Campos Obrigatorios (Placa)");
        }else {
            
             txtPlaca.setBorder(MetalBorders.getTextFieldBorder());
        }
        
        return validador;
    }
