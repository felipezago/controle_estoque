    public Double somar(){
        Double valor;
        Double soma= 0.0;
        
        List<Double> somas = new ArrayList<>();
        
        try {
            itv= ItensVendaDAO.listar();
            itv2 = ItensVendaDAO.listarServ();
            
            for(ItensVenda i: itv2){
                valor= i.getValorParcial();
               somas.add(valor);
            }

            for(ItensVenda i: itv){
                valor= i.getValorParcial();
                somas.add(valor);
            }

            System.out.println(somas);
            
            for(Double sum: somas){
                soma+= sum;
            }
            
            
            
            
            total.setVisible(true);
            total.setText(""+soma+"");
            
            
        } catch (SQLException ex) {
            
        }
        
        return soma;
    }
    
