    private void btnRemoveCestaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveCestaActionPerformed
        
        ItensVenda it= itv.get(tbItv.getSelectedRow());
        System.out.println(evt.getWhen());
        Double val= 0.0;
        val= subtrair(it);
        
        int qt= it.getQuantidade();
        if(qt != 1){
            try {
                    
                atualizaTabelaIt();
                ItensVendaDAO.alteraQuantidade(it);
                
                ItensVendaDAO.subtrai(val, it);
                
                ProdutosDAO.addEstoque(it);
                atualizaTabelaIt();
                atualizaTabelaProd();
                somar();
            } catch (SQLException ex) {
                
            }
                
        }else{
            int opc = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir o item: "+ it.getProduto().getNome()+" da cesta?");
              if(opc == JOptionPane.YES_OPTION){
                  try{
                      ProdutosDAO.addEstoque(it);
                      ItensVendaDAO.excluir(it);
                      
                      atualizaTabelaIt();

                  }catch(MySQLIntegrityConstraintViolationException ex){
                      JOptionPane.showMessageDialog(null, "Não pode excluir");
                  } catch (SQLException ex) {
                      JOptionPane.showMessageDialog(null, "Não pode excluir");
                  }catch(ArrayIndexOutOfBoundsException ex){
                      JOptionPane.showMessageDialog(null, "Por favor, selecione uma coluna da tabela primeiro.");
                  } 

              }
        }
        
    }//GEN-LAST:event_btnRemoveCestaActionPerformed

