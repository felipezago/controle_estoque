    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        Produtos p = lista.get(tbProduto.getSelectedRow());
        ItensVenda it= new ItensVenda();
        int idProd= p.getId();
        it.setProduto(p);
        it.setQuantidade(1);
        it.setValorParcial(p.getPreco());
        
        List<ItensVenda> itv = new ArrayList<>();
        int qt= 0;
        
        try {
            
            if(p.getProdserv().equals("Serviço")){
                 if(ItensVendaDAO.validaExist(it.getProduto().getId())){
                     ItensVendaDAO.alteraQt(it);
                
                    itv= ItensVendaDAO.listarProd(it.getProduto().getId());

                    for(ItensVenda i: itv){
                        qt= i.getQuantidade();
                        ItensVendaDAO.alteraValor(i, qt, idProd);             
                    }

                    atualizaTabelaServ();
                    somar();
                 }else{
                     ItensVendaDAO.inserir(it);
                     atualizaTabelaServ();
                    somar();
                 }
            atualizaTabelaServ();
            }else if(p.getEstoque() > 0){
                    if(ItensVendaDAO.validaExist(it.getProduto().getId())){
                        ItensVendaDAO.alteraQt(it);
                        ProdutosDAO.removeEstoque(p);

                        itv= ItensVendaDAO.listarProd(it.getProduto().getId());
                        for(ItensVenda i: itv){
                            qt= i.getQuantidade();
                            ItensVendaDAO.alteraValor(i, qt, idProd);             
                        }
                        somar();

                    }else{
                        ItensVendaDAO.inserir(it);
                        ProdutosDAO.removeEstoque(p);
                        somar();
                    }            

                atualizaTabelaProd();   
                atualizaTabelaIt();
                
            }else if(p.getEstoque() <= 0){
                JOptionPane.showMessageDialog(null, "Não foi possível adicionar, produto não se"
                        + " encontra em estoque.");
            }
       } catch (SQLException ex) {
           JOptionPane.showMessageDialog(null, "Erro ao adicionar");
           ex.printStackTrace();
        }catch(ArrayIndexOutOfBoundsException ex){
            JOptionPane.showMessageDialog(null, "Por favor, selecione uma coluna da tabela primeiro.");
        } 
       
        
    }//GEN-LAST:event_btnAddActionPerformed

