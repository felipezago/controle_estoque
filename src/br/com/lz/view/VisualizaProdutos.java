    private void btnExcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcActionPerformed
        Produtos p= lista.get(tbProduto.getSelectedRow());

        int opc = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir o Produto: "+ p.getNome()+"?");

        if(opc == JOptionPane.YES_OPTION){
            try{
                ProdutosDAO.excluir(p);
                atualizaTabela();

            }catch(MySQLIntegrityConstraintViolationException ex){
                JOptionPane.showMessageDialog(null, "Não pode excluir");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Não pode excluir, verifique se produto já está \nvinculado à uma venda");
            }

        }
    }//GEN-LAST:event_btnExcActionPerformed

    private void btnALTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnALTActionPerformed
        Produtos p= lista.get(tbProduto.getSelectedRow());
        
        if(p.getProdserv().equals("Serviço")){
            CadastroServicos cad= new CadastroServicos(p);
            cad.setVisible(true);

            btnExc.setEnabled(false);
            btnALT.setEnabled(false);
        }else{
            CadastroProdutos cad= new CadastroProdutos(p);
            cad.setVisible(true);

            btnExc.setEnabled(false);
            btnALT.setEnabled(false);
        }
        
        
 
    }//GEN-LAST:event_btnALTActionPerformed

