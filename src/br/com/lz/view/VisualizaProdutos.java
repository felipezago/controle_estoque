    private Produtos retornaObjeto() {
        Produtos produto = new Produtos();
        produto.setNome(txPesqProd.getText().toUpperCase()); 
        
        return produto;
    }
                                   
    private void atualizaTabela(){

        try {
            lista = ProdutosDAO.pesquisar(retornaObjeto());
            DefaultTableModel modelo = (DefaultTableModel) tbProduto.getModel();
            modelo.setNumRows(0);
            
            for (Produtos prod: lista) {
                if(prod.getProdserv().equals("S")){
                    prod.setProdserv("Serviço");
                     modelo.addRow(new Object[]{
                        prod.getId(),
                        prod.getNome(),
                        prod.getPreco(),
                        prod.getEstoque(),
                        prod.getProdserv()
                    });
                }else{
                    prod.setProdserv("Produto");
                    modelo.addRow(new Object[]{
                        prod.getId(),
                        prod.getNome(),
                        prod.getPreco(),
                        prod.getEstoque(),
                        prod.getProdserv()
                    
                    });
                }
            }

        } catch (SQLException e) {
            e.printStackTrace();
        }
    } 

